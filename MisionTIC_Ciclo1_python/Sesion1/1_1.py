#Utilización de las operaciones de comparación dentro de la salida print
print(A==B)
print(A!=B)
print(A>B)
print(A<B)

#Utilización de operadores Lógicos: And, Or, Not
print(A>B and A!=B)
print(A>B and A==B)
print(A>B or A==B)
print(A<B or A==B)
print(not(A<B) or A==B)

